myquarter <- function(x){
  
  r = ifelse(x>=0 & x<90, 1, 
         ifelse(x>=90 & x<180,2, 
         ifelse(x>=180 & x<270, 3, 4)))
  
  return(r)
}

myquarter(c(910, 492, 1900))


